def main() -> None:
    with DATA.open(encoding="utf-8") as f:
        data = json.load(f)

    entities = {e["name"] for e in data["entities"]}
    existing = {(r["source"], r["target"], r["type"]) for r in data["relations"]}

    added = 0
    for item in BRIDGE_RELATIONS:
        source, target, rtype, desc = item
        if source not in entities or target not in entities:
            print(f"跳过（实体不存在）: {source} -> {target}")
            continue
        key = (source, target, rtype)
        if key in existing:
            continue
        data["relations"].append(rel(source, target, rtype, desc))
        existing.add(key)
        added += 1

    with DATA.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"新增 {added} 条桥接关系，当前共 {len(data['relations'])} 条 → {DATA}")


if __name__ == "__main__":
    main()
